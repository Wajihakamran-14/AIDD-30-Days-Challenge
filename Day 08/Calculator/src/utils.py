def format_number(number):
    """Formats a number for display, ensuring consistent precision."""
    # For simplicity, we'll use a basic string conversion for now.
    # More complex formatting can be added later if needed.
    return str(number)

def handle_division_by_zero():
    """Returns an error message for division by zero."""
    return "Error: Division by zero"

def handle_overflow_underflow():
    """Returns an error message for overflow/underflow."""
    return "Error: Number out of range"
